package com.example.projectWork.dto;

public class AdminDTO {
    private String nome;
    private String email;

    // Costruttore vuoto
    public AdminDTO() {
    }

    // Getter e setter

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

}
